// Crie uma classe chamada Controle que especifique os métodos andar(), virar() e falar(). 

public class Controle 
{
    public void andar()
    {
        System.out.println("O objeto anda.\n");
    }
    public void virar()
    {
        System.out.println("O objeto vira.\n");
    }
    public void falar()
    {
        System.out.println("O objeto fala.\n");
    }
}